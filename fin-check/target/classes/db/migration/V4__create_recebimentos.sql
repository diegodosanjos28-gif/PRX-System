CREATE TABLE recebimentos (
    id                          UUID           NOT NULL DEFAULT gen_random_uuid() PRIMARY KEY,
    estabelecimento_id          UUID           NOT NULL REFERENCES estabelecimentos (id),
    id_conciflex                VARCHAR(50),
    cod_tipo_lancamento         VARCHAR(5),
    tipo_lancamento             VARCHAR(50),
    cod_tipo_pagamento          VARCHAR(5),
    tipo_pagamento              VARCHAR(50),
    data_venda                  DATE,
    data_previsao               DATE,
    data_pagamento              DATE,
    data_cancelamento           DATE,
    adquirente                  VARCHAR(100),
    cod_adquirente              VARCHAR(20),
    bandeira                    VARCHAR(100),
    cod_bandeira                VARCHAR(20),
    modalidade                  VARCHAR(100),
    nsu                         VARCHAR(50),
    autorizacao                 VARCHAR(50),
    cartao                      VARCHAR(50),
    numero_resumo_venda         VARCHAR(50),
    produto                     VARCHAR(100),
    meio_captura                VARCHAR(50),
    valor_bruto                 NUMERIC(15,2),
    taxa_percentual             NUMERIC(10,5),
    valor_taxa                  NUMERIC(15,6),
    tarifa_transacao            NUMERIC(15,2),
    taxa_antecipacao            NUMERIC(8,4),
    valor_taxa_antecipacao      NUMERIC(15,6),
    outras_despesas             NUMERIC(15,2),
    valor_liquido               NUMERIC(18,6),
    valor_liquido_s_antecipacao NUMERIC(15,6),
    parcela                     SMALLINT,
    total_parcelas              SMALLINT,
    possui_taxa_minima          CHAR(1),
    estabelecimento_conciflex   VARCHAR(255),
    banco                       VARCHAR(100),
    agencia                     VARCHAR(20),
    conta_corrente              VARCHAR(30),
    status_conciliacao          VARCHAR(50),
    codigo_operadora_ajuste     VARCHAR(50),
    desc_ajuste                 TEXT,
    classificacao_ajuste        VARCHAR(100),
    autorizador                 VARCHAR(50),
    data_processamento          DATE,
    hora_processamento          VARCHAR(8),
    nome_arquivo                VARCHAR(255),
    observacoes                 TEXT,
    coletado_em                 TIMESTAMP
);

CREATE INDEX idx_rec_estabelecimento_data ON recebimentos (estabelecimento_id, data_pagamento);
CREATE INDEX idx_rec_bandeira             ON recebimentos (bandeira);
CREATE INDEX idx_rec_id_conciflex        ON recebimentos (id_conciflex);
